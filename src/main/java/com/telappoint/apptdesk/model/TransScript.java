package com.telappoint.apptdesk.model;

public class TransScript {
	private String fileName;
	private long transScriptId;
	
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public long getTransScriptId() {
		return transScriptId;
	}
	public void setTransScriptId(long transScriptId) {
		this.transScriptId = transScriptId;
	}
}
